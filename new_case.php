        <h2>Famous Cybersecurity Incidents Resulting in Data Breaches</h2>
        <ol>
          <li>
          <a href="https://www.ftc.gov/enforcement/refunds/equifax-data-breach-settlement"> <h3>Equifax Data Breach (2017):</h3></a>
            <p>Equifax, one of the largest credit reporting agencies, experienced a massive data breach that exposed the personal information of approximately 147 million people. The breach included sensitive data such as Social Security numbers, birth dates, addresses, and in some cases, driver's license numbers.</p>
          </li>
          <li>
            <a href="https://www.nytimes.com/2017/10/03/technology/yahoo-hack-3-billion-users.html" ><h3>Yahoo Data Breaches (2013-2016):</h3></a>
            <p>Yahoo suffered a series of data breaches affecting billions of user accounts over several years. The breaches compromised a wide range of personal information, including names, email addresses, phone numbers, and hashed passwords.</p>
          </li>
          <li>
          <a href="https://www.capitalone.com/digital/facts2019/">  <h3>Capital One Data Breach (2019):</h3></a>
            <p>Capital One experienced a data breach that exposed the personal information of over 100 million customers in the United States and Canada. The breach involved unauthorized access to sensitive data, including names, addresses, credit scores, and Social Security numbers.</p>
          </li>
          <li>
            <a href="https://news.marriott.com/news/2018/11/30/marriott-announces-starwood-guest-reservation-database-security-incident"><h3>Marriott International Data Breach (2018):</h3></a>
            <p>Marriott International disclosed a data breach affecting approximately 500 million guests of its Starwood hotel reservation system. The breach exposed personal information, including names, passport numbers, email addresses, and encrypted payment card details.</p>
          </li>
          <li>
            <a href="https://redriver.com/security/target-data-breach"><h3>Target Data Breach (2013):</h3></a>
            <p>Target Corporation suffered a data breach during the holiday shopping season that compromised the payment card information of approximately 40 million customers. The breach also exposed personal data, including names, addresses, and email addresses, of an additional 70 million customers.</p>
          </li>
          <li>
            <a href="https://www.theguardian.com/technology/2011/apr/26/playstation-network-hackers-data"><h3>Sony PlayStation Network Breach (2011):</h3></a>
            <p>Sony's PlayStation Network (PSN) experienced a cyberattack that resulted in the theft of personal and financial information belonging to millions of users. The breach exposed names, addresses, email addresses, and encrypted credit card information.</p>
          </li>
          <li>
         <a href=https://help.uber.com/riders/article/information-about-2016-data-security-incident?nodeId=12c1e9d1-4042-4231-a3ec-3605779b8815">   <h3>Uber Data Breach (2016):</h3></a>
            <p>Uber disclosed a data breach that occurred in 2016, affecting approximately 57 million users worldwide. The breach involved the theft of personal information, including names, email addresses, and phone numbers, of both riders and drivers.</p>
          </li>
          <li>
          <a href="https://www.insurance.ca.gov/0400-news/0100-press-releases/anthemcyberattack.cfm">  <h3>Anthem Data Breach (2015):</h3></a>
            <p>Anthem Inc., one of the largest health insurance companies in the United States, experienced a data breach that exposed the personal information of nearly 80 million individuals. The breach included names, Social Security numbers, dates of birth, and other sensitive data.</p>
          </li>
        </ol>
